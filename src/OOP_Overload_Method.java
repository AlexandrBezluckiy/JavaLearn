import JavaLearn.Method;

public class OOP_Overload_Method {
    public static void main(String[] args) {
        System.out.println(Method.calc_mid(2, 4));
        System.out.println(Method.calc_mid(3, 4, 6));
        System.out.println(Method.calc_mid(3, 4, 6, 8));
        System.out.println(Method.calc_mid(3, 4, 6, 8, 9, 15, 1, 58, 3, 4, 24, 7));
    }
}
